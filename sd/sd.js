// keep a global reference to iframe as it will be needed multiple times
myIframe = document.getElementById("my-iframe");

function populateNavbar()
{
    // get reference to navbar
    var myNavbar = document.getElementById("my-navbar");
	for (const item of journalDetails)
    {
		var person = item[0];
		var href = item[1];
        // add a button for each person
        // make it call main on click with event
		if(person.startsWith("sep")) {
			console.log('you saw sep');
			myNavbar.innerHTML += `<hr><b>${href}</b><hr>`;
			
		} else {
			myNavbar.innerHTML += `<a href='${href}' data-href='${href}' onclick='showIframe(event)'>${person}</a>`;
		}
    }
}

// we want to highlight which name was just clicked, to keep track of where in the index we currently are
// to implement it we will have to clear this hightlight from previously clicked name
// so keep last event handy to modify its highlight
var lastEvent = null;

function showIframe(currentEvent)
{
	var target = currentEvent.currentTarget || currentEvent.target;

    // clear highlight of last clicked name
    if (lastEvent)
    {
        lastEvent.target.style.color = "#8ab4f8";
        lastEvent.target.style.backgroundColor = "transparent";
    }

    // apply highlight to current clicked name
	target.style.color = "black";
	target.style.backgroundColor = "#8ab4f8";

    // store current event as last event
	lastEvent = { target: target };

	var href = target.getAttribute("data-href") || target.getAttribute("href");
    
	// load iframe for the clicked entry
	myIframe.innerHTML = `<iframe src="${href}" style="width: 100%; height: 100vh;"></iframe>`;

    // prevent link from opening
    currentEvent.preventDefault();
}

// ── Theme toggle (day / night) ──
// Call this once per stotra page after DOM is ready.
// Remembers choice for the current browser session only.
function initThemeToggle() {
	var STORAGE_KEY = "sd-theme";
	var root = document.documentElement;
	var toggle = document.getElementById("themeToggle");
	if (!toggle) return;
	var currentTheme = sessionStorage.getItem(STORAGE_KEY) === "night" ? "night" : "day";

	function applyTheme(theme) {
		if (theme === "night") {
			root.setAttribute("data-theme", "night");
			toggle.textContent = "\u2600\ufe0f Day";
		} else {
			root.removeAttribute("data-theme");
			toggle.textContent = "\uD83C\uDF19 Night";
		}
		sessionStorage.setItem(STORAGE_KEY, theme);
		currentTheme = theme;
	}

	applyTheme(currentTheme);
	toggle.addEventListener("click", function () {
		applyTheme(currentTheme === "day" ? "night" : "day");
	});
}

// Apply smaller reusable styling for Tamil blocks where present.
function applyTamilVerseStyles() {
	var tamilRegex = /[\u0B80-\u0BFF]/;
	var tamilCharRegex = /[\u0B80-\u0BFF]/g;
	var devanagariCharRegex = /[\u0900-\u097F]/g;
	var selectors = [
		".stotra-text .verse",
		".stotra-text p"
	];
	var nodes = document.querySelectorAll(selectors.join(","));
	nodes.forEach(function (node) {
		if (tamilRegex.test(node.textContent || "")) {
			node.classList.add("tamil-verse");
		}
	});

	var containers = document.querySelectorAll(".stotra-text");
	containers.forEach(function (container) {
		if (container.querySelector(".tamil-verse")) {
			return;
		}
		var text = container.textContent || "";
		var tamilChars = (text.match(tamilCharRegex) || []).length;
		var latinChars = (text.match(/[A-Za-z]/g) || []).length;
		var devanagariChars = (text.match(devanagariCharRegex) || []).length;
		if (tamilChars > 0 && tamilChars >= latinChars * 2 && devanagariChars === 0) {
			container.classList.add("tamil-verse");
		}
	});
}

